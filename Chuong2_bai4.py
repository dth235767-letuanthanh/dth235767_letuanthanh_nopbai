'''
Câu 4: Python hỗ trợ những kiểu dữ liệu cơ bản nào?
'''
# Kiểu dữ liệu số nguyên (Integer)
x = 10  # int: Số nguyên, ví dụ: 10

# Kiểu dữ liệu số thực (Floating-point)
y = 3.14  # float: Số thực, ví dụ: 3.14

# Kiểu dữ liệu số phức (Complex number)
z = 2 + 3j  # complex: Số phức, ví dụ: 2 + 3j

# Kiểu dữ liệu chuỗi (String)
name = "Alice"  # str: Chuỗi, ví dụ: "Alice"

# Kiểu dữ liệu Boolean (Logic)
is_active = True  # bool: Giá trị logic, ví dụ: True

# Kiểu dữ liệu None (Không có giá trị)
nothing = None  # NoneType: Không có giá trị, ví dụ: None

# Kiểu dữ liệu danh sách (List)
fruits = ["apple", "banana", "cherry"]  # list: Danh sách, ví dụ: ["apple", "banana"]

# Kiểu dữ liệu bộ (Tuple)
coordinates = (10, 20)  # tuple: Bộ, ví dụ: (10, 20)

# Kiểu dữ liệu từ điển (Dictionary)
person = {"name": "Alice", "age": 25}  # dict: Từ điển, ví dụ: {"name": "Alice", "age": 25}

# Kiểu dữ liệu tập hợp (Set)
numbers = {1, 2, 3, 4}  # set: Tập hợp, ví dụ: {1, 2, 3, 4}

# Kiểu dữ liệu frozenset (Tập hợp bất biến)
frozen_numbers = frozenset([1, 2, 3, 4])  # frozenset: Tập hợp bất biến, ví dụ: frozenset([1, 2, 3])